package _02ejemplos;
public class _02SalidaPantalla {
    public static void main(String args[]){
        // Mostrar texto en varias líneas
        System.out.println("Homer");
        System.out.println("J.");
        System.out.println("Simpson");
        
        // Mostrar texto en una línea
        System.out.print("Homer ");
        System.out.print("J. ");
        System.out.println("Simpson");
        
        //También se pueden mostrar números
        System.out.println("125");
        System.out.println(125);
        System.out.println("125+250");
        System.out.println(125+250);
        // No es lo mismo meter números en modo texto ("xxx") que números en modo número (xxx)
    }
    
}
